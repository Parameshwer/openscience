						<p><strong>Publication Ethics:</strong><br>
						The publication of an article in a peer-reviewed journal is an essential building block in the development of respected network of knowledge. It depends upon the work of the authors and the institutions that support them. Peer-reviewed articles support and embody the scientific methods and therefore it is necessary to follow the for all the authors, journal editors, publisher, peer-reviewers and the public involved in the publishing act. We follow COPE guidelines to ensure the journal quality and maintain high standards for our journals’ publications.</p>
						<p><strong>Plagiarism/Duplicate Submission:</strong><br>
							All journals published by Open Science Publications are committed to publishing only original material, i.e., material that has neither been published elsewhere, nor is under review elsewhere.<br>Open Science Publications is using to check the plagiarism in the online plagiarism checker.<br>Manuscripts that are found to have been plagiarized or duplicated will be rejected immediately.</p>
							<p><strong>Citation Manipulation</strong><br>
							Submitted manuscripts that are found to include citations whose primary purpose is to increase the number of citations to a given author’s work, or to articles published in a particular journal, will incur citation manipulation sanctions.</p>
							<p><strong>Conflicts of Interest</strong><br>
							A conflict of interest (COI) is a situation in which a person or organization is involved in multiple interests (financial, emotional, or otherwise), one of which could possibly corrupt the motivation of the individual or organization.</p>
							<p><strong>Copyright</strong><br>
							Submission of a manuscript implies that the work described has not been published before (except in the form of an abstract or as part of a published lecture, or thesis) and that it is not under consideration for publication elsewhere.</p>
							<p>All works published by open science publications are under the terms of the Creative Commons Attribution License. This permits anyone to copy, distribute, transmit and adapt the work provided the original work and source is appropriately cited.</p>
							<p><strong>Open access policies</strong><br>
								The publication costs are usually covered by the author’s institution or research funds.<br>
							Open access articles are freely available online to the interested online readers without any financial (or) legal. At the same time, authors who publish in Open Access journals retain the copyright of their article.</p>
							<p><strong>Pricing policy</strong><br>
							All the articles which are published under an Open Access policy are permanently free to be read by everyone. These articles require an Article Publishing Charge (APC) to be paid either by the author or by their institution or funder which covers the editorial and peer-review services, website hosting and administrative costs.</p>
							<p><strong>Waiver policy</strong><br>
							Waiver policy is applicable only upon the request of authors and priority will be given to the requests by authors from the low-income countries as per the World Bank classification.</p>
							<p><strong>Peer-review policy</strong><br>
							All submitted manuscripts to Open Science journals follow single blinded peer-review processing where the information of the reviewers is hidden from the authors.</p>
